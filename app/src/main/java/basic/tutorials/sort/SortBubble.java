package basic.tutorials.sort;

/**
 * バブルソート (Bubble Sort)
 *
 * 考え方:
 * 1. 配列の隣り合う2つの要素を比較する。
 * 2. 左側の要素が右側の要素より大きい場合、それらを交換する。
 * 3. この操作を配列の末尾まで繰り返す。この1回の走査で、配列中の最大値が末尾に移動する（泡が水面に上がっていく様子に似ている）。
 * 4. 走査範囲を狭め（末尾のソート済み要素を除く）、1〜3の処理を繰り返す。
 * これを配列全体がソート済みになるまで続ける。
 */
public class SortBubble {

    /**
     * バブルソートアルゴリズムを使用して整数配列をソートします。
     * @param array ソートする整数配列
     * @return ソートされた整数配列
     */
    public static int[] sort(int[] array) {
        // 外側のループ。配列の要素数-1回繰り返す
        // 1回のループで最も大きい要素が配列の末尾に移動（確定）するため、
        // 次のループでは走査範囲を1つ狭めることができる
        for (int i = 0; i < array.length - 1; i++) {
            // 内側のループ。隣接する要素を比較・交換する
            // i回ループが終わるごとに、末尾からi個の要素はソート済みになるので、走査範囲から除外する
            for (int j = 0; j < array.length - i - 1; j++) {
                // 左側の要素が右側の要素より大きい場合、それらを交換する
                if (array[j] > array[j + 1]) {
                    // 隣接する要素を交換
                    int temp = array[j];
                    array[j] = array[j + 1];
                    array[j + 1] = temp;
                }
            }
        }
        return array;
    }
}
