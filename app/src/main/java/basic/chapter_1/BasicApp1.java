package basic.chapter_1;

/**
 * 第1章: Javaの世界へようこそ！
 * プログラムの基本構造と、ユーザーからの入力を受け取る方法を学びます。
 *
 * 【このコードで学ぶこと】
 * 1. class, mainメソッドの基本構造。
 * 2. 命令文（ステートメント）の終わりにセミコロンを付けること。
 * 3. System.out.println()で文字列を画面に表示する方法。
 * 4. 一行コメントと複数行コメントの使い方。
 */
public class BasicApp1 {

    public static void main(String[] args) {
        // --- 画面への出力 ---
        // System.out.println() は、()の中の文字列を画面に表示して改行する命令です。
        // 命令文 (ステートメント) の最後には、必ずセミコロン(;)を付けます。
        System.out.println("Hello, Java World!");

        // --- 1行コメント ---
        /*
         * このように、／＊（半角） と ＊／（半角） で囲むと、
         * 複数行にわたってコメントを書くことができます。
         * コメントはプログラムの動作に影響を与えません。
         */
    }
}